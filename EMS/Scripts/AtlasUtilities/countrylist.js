function G_CountryStates(CountryDOM, StateDOM) {
    $.ajax({
        url: APIUrlGetVendorStateList + '?CountryID=' + cntryList.find((e) => { return e.name == $(CountryDOM).val(); }).ID,
        cache: false,
        beforeSend: function (request) {
            request.setRequestHeader("Authorization", localStorage.EMSKeyToken);
        },
        type: 'POST',
        contentType: 'application/json; charset=utf-8',
    })
    .done(function (response) {
        var array = [];
        array = response.error ? [] : $.map(response, function (m) {
            return {
                value: m.StateName,
                label: m.StateName,

            };
        });

        $(StateDOM).autocomplete({
            minLength: 1,
            source: array,
            autoFocus: true,
            select: function (event, ui) {
                $(this).val(ui.item.label);
                $(this).attr('name', ui.item.value);
                return false;
            }
        })
    })
    .fail(function (error) {
        ShowMSG(error);
    })
}